package ddit.vo;

public class CustomerCenterVO {
	private int cc_no;
	private String member_id;
	private String cc_title;
	private String cc_comment;
	private String cc_date;
	private String cc_content;
	private String cc_type;
	private int cc_status;
	
	public int getCc_no() {
		return cc_no;
	}
	public void setCc_no(int cc_no) {
		this.cc_no = cc_no;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getCc_title() {
		return cc_title;
	}
	public void setCc_title(String cc_title) {
		this.cc_title = cc_title;
	}
	public String getCc_comment() {
		return cc_comment;
	}
	public void setCc_comment(String cc_comment) {
		this.cc_comment = cc_comment;
	}
	public String getCc_date() {
		return cc_date;
	}
	public void setCc_date(String cc_date) {
		this.cc_date = cc_date;
	}
	public String getCc_content() {
		return cc_content;
	}
	public void setCc_content(String cc_content) {
		this.cc_content = cc_content;
	}
	public String getCc_type() {
		return cc_type;
	}
	public void setCc_type(String cc_type) {
		this.cc_type = cc_type;
	}
	public int getCc_status() {
		return cc_status;
	}
	public void setCc_status(int cc_status) {
		this.cc_status = cc_status;
	}
	
	
	
}
