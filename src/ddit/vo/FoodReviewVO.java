package ddit.vo;

public class FoodReviewVO {

	private String f_reviewno  ;
	private String food_code   ;
	private String f_content   ;
	private int f_star      ;
	private String f_pic       ;
	private String f_date      ;
	private String member_id   ;
	
	
	public String getF_reviewno() {
		return f_reviewno;
	}
	public void setF_reviewno(String f_reviewno) {
		this.f_reviewno = f_reviewno;
	}
	public String getFood_code() {
		return food_code;
	}
	public void setFood_code(String food_code) {
		this.food_code = food_code;
	}
	public String getF_content() {
		return f_content;
	}
	public void setF_content(String f_content) {
		this.f_content = f_content;
	}
	public int getF_star() {
		return f_star;
	}
	public void setF_star(int f_star) {
		this.f_star = f_star;
	}
	public String getF_pic() {
		return f_pic;
	}
	public void setF_pic(String f_pic) {
		this.f_pic = f_pic;
	}
	public String getF_date() {
		return f_date;
	}
	public void setF_date(String f_date) {
		this.f_date = f_date;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	
	
	
	
}
