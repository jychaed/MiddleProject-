package ddit.vo;

public class TripVO {
	private String trip_no;
	private String member_id;
	private String trip_title;
	private String trip_start;
	private String trip_end;
	private String trip_city;
	private int trip_people;
	
	public String getTrip_no() {
		return trip_no;
	}
	public void setTrip_no(String trip_no) {
		this.trip_no = trip_no;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getTrip_title() {
		return trip_title;
	}
	public void setTrip_title(String trip_title) {
		this.trip_title = trip_title;
	}
	public String getTrip_start() {
		return trip_start;
	}
	public void setTrip_start(String trip_start) {
		this.trip_start = trip_start;
	}
	public String getTrip_end() {
		return trip_end;
	}
	public void setTrip_end(String trip_end) {
		this.trip_end = trip_end;
	}
	public String getTrip_city() {
		return trip_city;
	}
	public void setTrip_city(String trip_city) {
		this.trip_city = trip_city;
	}
	public int getTrip_people() {
		return trip_people;
	}
	public void setTrip_people(int trip_people) {
		this.trip_people = trip_people;
	}
	
}
